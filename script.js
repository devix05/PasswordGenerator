let currentTheme = localStorage.getItem("theme") || "light";
let currentLanguage = localStorage.getItem("language") || "de";
let currentSection = 0;

const themeToggle = document.getElementById("themeToggle");
const langButtons = document.querySelectorAll(".lang-btn");
const burgerMenu = document.getElementById("burgerMenu");
const mobileNav = document.getElementById("mobileNav");
const mobileNavOverlay = document.getElementById("mobileNavOverlay");
const closeMobileNav = document.getElementById("closeMobileNav");
const navLinks = document.querySelectorAll(
  ".nav-link, .mobile-nav-link, [data-section]",
);
const sections = document.querySelectorAll(".section");
const sectionNav = document.querySelector(".section-nav");

function setupEmailCopy(buttonId, email) {
  const button = document.getElementById(buttonId);
  if (!button) return;

  button.addEventListener("click", async (e) => {
    e.preventDefault();
    try {
      await navigator.clipboard.writeText(email);

      const icon = button.querySelector("i");
      const originalIcon = icon.className;
      icon.className = "fas fa-check";
      button.classList.add("copied");

      const successMessage =
        currentLanguage === "de" ? "📧 Email kopiert!" : "📧 Email copied!";
      showNotification(successMessage);

      setTimeout(() => {
        icon.className = originalIcon;
        button.classList.remove("copied");
      }, 2000);
    } catch (err) {
      const textarea = document.createElement("textarea");
      textarea.value = email;
      document.body.appendChild(textarea);
      textarea.select();
      document.execCommand("copy");
      document.body.removeChild(textarea);

      const fallbackMessage =
        currentLanguage === "de" ? "📧 Email kopiert!" : "📧 Email copied!";
      showNotification(fallbackMessage);
    }
  });
}

document.documentElement.setAttribute("data-theme", currentTheme);
updateThemeIcon();

updateLanguage(currentLanguage);
document
  .querySelector(`[data-lang="${currentLanguage}"]`)
  .classList.add("active");

themeToggle.addEventListener("click", () => {
  currentTheme = currentTheme === "light" ? "dark" : "light";
  document.documentElement.setAttribute("data-theme", currentTheme);
  localStorage.setItem("theme", currentTheme);
  updateThemeIcon();
});

function updateThemeIcon() {
  const icon = themeToggle.querySelector("i");
  icon.className = currentTheme === "light" ? "fas fa-moon" : "fas fa-sun";
}

langButtons.forEach((btn) => {
  btn.addEventListener("click", () => {
    const lang = btn.dataset.lang;
    if (lang === currentLanguage) return;

    currentLanguage = lang;
    localStorage.setItem("language", lang);

    langButtons.forEach((b) => b.classList.remove("active"));
    btn.classList.add("active");

    updateLanguage(lang);
  });
});

function updateLanguage(lang) {
  const elements = document.querySelectorAll("[data-i18n]");
  elements.forEach((element) => {
    const key = element.dataset.i18n;
    if (translations[lang] && translations[lang][key]) {
      if (element.tagName === "INPUT" || element.tagName === "TEXTAREA") {
        element.placeholder = translations[lang][key];
      } else {
        element.textContent = translations[lang][key];
      }
    }
  });

  document.documentElement.setAttribute("data-language", lang);
}

function toggleMobileNav() {
  mobileNav.classList.toggle("active");
  mobileNavOverlay.classList.toggle("active");
  burgerMenu.classList.toggle("active");
  document.body.style.overflow = mobileNav.classList.contains("active")
    ? "hidden"
    : "";
}

burgerMenu.addEventListener("click", toggleMobileNav);
closeMobileNav.addEventListener("click", toggleMobileNav);
mobileNavOverlay.addEventListener("click", toggleMobileNav);

function switchSection(sectionIndex) {
  sections.forEach((section) => {
    section.classList.remove("active");
  });

  sections[sectionIndex].classList.add("active");
  currentSection = sectionIndex;

  navLinks.forEach((link) => {
    link.classList.remove("active");
    if (link.dataset.section == sectionIndex) {
      link.classList.add("active");
    }
  });

  const dots = document.querySelectorAll(".section-dot");
  dots.forEach((dot, index) => {
    if (index === sectionIndex) {
      dot.classList.add("active");
    } else {
      dot.classList.remove("active");
    }
  });

  if (mobileNav.classList.contains("active")) {
    toggleMobileNav();
  }
}

navLinks.forEach((link) => {
  link.addEventListener("click", (e) => {
    e.preventDefault();
    const sectionIndex = link.dataset.section;
    if (sectionIndex !== undefined) {
      switchSection(parseInt(sectionIndex));
    }
  });
});

function createSectionNav() {
  if (!sectionNav) return;

  const sections = document.querySelectorAll(".section");
  sectionNav.innerHTML = "";

  sections.forEach((section, index) => {
    const dot = document.createElement("div");
    dot.className = "section-dot";
    if (index === currentSection) {
      dot.classList.add("active");
    }
    dot.addEventListener("click", () => {
      switchSection(index);
    });
    sectionNav.appendChild(dot);
  });
}

const contactForm = document.querySelector(".contact-form form");
if (contactForm) {
  const inputs = contactForm.querySelectorAll(
    "input[required], textarea[required]",
  );

  inputs.forEach((input) => {
    input.addEventListener("invalid", (e) => {
      e.preventDefault();
      input.classList.add("error");
      showNotification("Bitte fülle alle Pflichtfelder aus ⚠️");
    });

    input.addEventListener("input", () => {
      if (input.validity.valid) {
        input.classList.remove("error");
      }
    });
  });

  contactForm.addEventListener("submit", (e) => {
    e.preventDefault();

    let isValid = true;
    inputs.forEach((input) => {
      if (!input.value.trim()) {
        isValid = false;
        input.classList.add("error");
      }
    });

    if (isValid) {
      const formData = new FormData(contactForm);

      fetch(contactForm.action, {
        method: "POST",
        body: formData,
        headers: {
          Accept: "application/json",
        },
      })
        .then((response) => {
          if (response.ok) {
            showNotification(
              translations[currentLanguage]["contact.form.success"] ||
                "Nachricht gesendet! ✨",
            );
            contactForm.reset();
          } else {
            showNotification(
              "Fehler beim Senden. Bitte versuche es später erneut. ❌",
            );
          }
        })
        .catch((error) => {
          showNotification(
            "Fehler beim Senden. Bitte versuche es später erneut. ❌",
          );
        });
    } else {
      showNotification("Bitte fülle alle Pflichtfelder aus ⚠️");
    }
  });
}

function showNotification(message) {
  const existingNotification = document.querySelector(".notification");
  if (existingNotification) {
    existingNotification.remove();
  }

  const notification = document.createElement("div");
  notification.className = "notification";
  notification.innerHTML = `
        <i class="fas fa-check-circle"></i>
        <span>${message}</span>
    `;
  document.body.appendChild(notification);

  setTimeout(() => {
    notification.classList.add("show");
  }, 10);

  setTimeout(() => {
    notification.classList.remove("show");
    setTimeout(() => {
      notification.remove();
    }, 300);
  }, 3000);
}

